#include <stdio.h>

int digitSum(int n) {
    int sum = 0;

    while (n > 0) {
        sum = sum + (n % 10);
        n = n / 10;
    }

    return sum;
}

int main() {
    int N;

    printf("Enter a number: ");
    scanf("%d", &N);

    int result = digitSum(N);

    printf("Sum of digits: %d", result);

    return 0;
}
